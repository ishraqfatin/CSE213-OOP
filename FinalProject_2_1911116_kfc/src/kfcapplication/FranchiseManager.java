public class FranchiseManager extends User implements Serializable {

    public FranchiseManager(int id, String name, String userType, String email, String password, String branch, String phone) {
        super(id, name, userType, email, password, branch, phone);
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUserType() {
        return userType;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    @Override
    public String toString() {
        return "id=" + id + ", name=" + name + ", pass="+password 
                    +", desig=" + userType
                    +", email="+email
                    + ", branch=" + branch
                    +"\n";
    }

}
